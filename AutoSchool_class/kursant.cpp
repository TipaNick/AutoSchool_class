#include "kursant.h"

kursant::kursant()
{
	fio = "П.П.Пример";
	age = 18;
	email = "ppp@mail.ru";
	phone = "880055535350";
	category = "Б";
}

kursant::kursant(string fio, int age, string email, string phone, string category)
{
	this->fio = fio;
	this->age = age;
	this->email = email;
	this->phone = phone;
	this->category = category;
}

void kursant::input()
{
	cout << "Введите ФИО: ";
	cin >> fio;
	cout << "Введите возраст: ";
	cin >> test_age;
	try
	{
		age = stoi(test_age);
	}
	catch (invalid_argument)
	{
		cout << "Не правильный ввод\n";
	}

	if (age < 16)
	{
		try {
			throw -1;
		}
		catch (int a) {
			if (a == -1)
			{
				cout << "Возраст меньше 16\n";
			}
		}
	}

	cout << "Введите почту: ";
	cin >> email;
	cout << "Введите телефон: ";
	cin >> phone;
	cout << "Введите категорию: ";
	cin >> category;
}

